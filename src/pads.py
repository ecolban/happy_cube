from enum import StrEnum, Enum


class Pad:
    def __init__(self, pad_str: str):
        lines = pad_str.strip().splitlines()
        self._pad_pieces = '\n'.join(line.strip(' ') for line in lines).split('\n\n')

    def __getitem__(self, item):
        return self._pad_pieces[item]

    def __iter__(self):
        return iter(self._pad_pieces)


